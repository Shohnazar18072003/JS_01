// Sonlar berilgan shu sonlar ichidan faqat murakkab sonlarni chiqaruvchi dastur tuzing (for)! 

function generateComplexNumbers(numbers) {
  var complexNumbers = [];

  for (var i = 0; i < numbers.length; i++) {
    var complexNumber = numbers[i] + "i";
    complexNumbers.push(complexNumber);
  }

  return complexNumbers;
}

var givenNumbers = [2, 5, 8];
var complexNumbers = generateComplexNumbers(givenNumbers);
